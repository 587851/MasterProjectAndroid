package com.example.masterprojectandroid.enums

/**
 * Enum class representing different types of health observations
 * supported by the app for collection, mapping, and uploading to FHIR.
 */
enum class ObservationType {
    BASAL_BODY_TEMPERATURE,
    BASAL_METABOLIC_RATE,
    BODY_FAT,
    BODY_TEMPERATURE,
    DISTANCE,
    HEART_RATE,
    HEART_RATE_VARIABILITY,
    OXYGEN_SATURATION,
    RESPIRATORY_RATE,
    RESTING_HEART_RATE,
    SLEEP,
    STEPS,
    VO2_MAX
}

